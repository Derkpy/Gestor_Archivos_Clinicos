    private void configurarModo(String modo) {
        if (modo.equals("nueva_consulta")) {
            // Mostrar "Generar" e "Imprimir", ocultar "Descargar"
            btn_dowloand.setVisible(false);
            jButton1.setVisible(true);
            imprimir.setVisible(true);
            // Los campos están habilitados por defecto
        } else if (modo.equals("ver_consulta")) {
            // Mostrar "Descargar", ocultar "Generar" e "Imprimir"
            btn_dowloand.setVisible(true);
            jButton1.setVisible(false);
            imprimir.setVisible(false);
            // Bloquear los campos de texto
            jTextField1.setEditable(false);
            jTextArea1.setEditable(false);
            jTextArea2.setEditable(false);
            jTextArea3.setEditable(false);
        }
    }
    
